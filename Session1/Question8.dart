// What is the difference between the num and double data types in Dart?
void main() {
  //num: ينفع يتخزن فيها ارقام صحيحة وارقام عشريه
  // double: تخزن فقط الاعداد العشرية
  // example
  num value1 = 10; //  int داخل num
  num value2 = 10.5; //  double داخل num

  double value3 = 99.99; //  double فقط

  print(value1);
  print(value2);
  print(value3);
}
