//  What does it mean for Dart to be a statically-typed language, and how does it
 // impact the way variables are used?
void main() {
 
//In Dart, you must specify the type of a variable when declaring it,
// and this type cannot be changed later. This is a fundamental rule in Dart  .


  int age = 23; 
  // age = "twenty three "; //  : لا يمكن تغيير `int` إلى `String`
  print(age);
}


