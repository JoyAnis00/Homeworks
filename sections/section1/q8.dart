// num is a superclass for both int and double.
// double is specifically for floating-point numbers.
void main() {
  num number1 = 10; // Can hold int
  num number2 = 10.5; // Can hold double
  double decimal = 12.34; // Specifically for floating-point numbers
  print("Num examples: $number1, $number2");
  print("Double example: $decimal");
}